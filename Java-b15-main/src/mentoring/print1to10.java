package mentoring;

public class print1to10 {
    public static void main(String[] args) {
        /*
        print numbers between 1 and 10  using a while loop
        1
        2
        3
        ...
        10
         */

        int i = 1;

        while(i <=10){
            System.out.println(i);
            i++;
        }
    }
}
